/* definitions for ANSI and VT100 escape codes go here */

/* normal colours */

#define ANSI_NORMAL "\033[0m"
#define ANSI_BLACK  "\033[30m"
#define ANSI_RED    "\033[31m"
#define ANSI_GREEN  "\033[32m"
#define ANSI_ORANGE "\033[33m"
#define ANSI_YELLOW "\033[33m"
#define ANSI_BLUE   "\033[34m"
#define ANSI_MAGENTA "\033[35m"
#define ANSI_VIOLET  "\033[35m"
#define ANSI_CYAN    "\033[36m"
#define ANSI_WHITE   "\033[37m"
#define ANSI_CLS     "\033[2J\033[H"

/* Da bright colours */

#define ANSI_BRIGHT_BLACK "\033[30;1m"
#define ANSI_BRIGHT_RED "\033[31;1m"
#define ANSI_BRIGHT_GREEN "\033[32;1m"
#define ANSI_BRIGHT_ORANGE "\033[33;1m"
#define ANSI_BRIGHT_YELLOW "\033[33;1m"
#define ANSI_BRIGHT_BLUE "\033[34;1m"
#define ANSI_BRIGHT_MAGENTA "\033[35;1m"
#define ANSI_BRIGHT_CYAN "\033[36;1m"
#define ANSI_BRIGHT_WHITE "\033[37;1m"

/* da background colours */

#define ANSI_BLACK_BACK "\033[40m"
#define ANSI_RED_BACK   "\033[41m"
#define ANSI_GREEN_BACK "\033[42m"
#define ANSI_YELLOW_BACK "\033[43m"
#define ANSI_BLUE_BACK  "\033[44m"
#define ANSI_MAGENTA_BACK "\033[45m"
#define ANSI_CYAN_BACK "\033[46m"
#define ANSI_WHITE_BACK "\033[47m"



